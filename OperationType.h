#ifndef OPERATIONTYPE_H
#define OPERATIONTYPE_H

enum class OperationType {
    ADDITION       = 0x00,
    SUBTRACT       = 0x01,
    MULTIPLICATION = 0x02,
    DIVISION       = 0x03,
    AND            = 0x04,
    OR             = 0x05,
    XOR            = 0x06,
    NAND           = 0x07,
};


#endif // OPERATIONTYPE_H
